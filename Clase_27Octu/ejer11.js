// switch con 5 casos

let carros = 10;
switch (carros) {
    case 5:
        console.log("Hay 5 carros");
        break;
    case 10:
        console.log("Hay 10 carros");
        break;
    case 15:
        console.log("Hay 15 carros");
        break;
    case 20:
        console.log("Hay 20 carros");
        break;
    case 25:
        console.log("Hay 25 carros");
        break;
    default:
        console.log("No hay tantos carros");
}