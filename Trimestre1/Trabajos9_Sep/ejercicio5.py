print("Ingrese tres números para promediarlos:\n") # Pedimos al usuario ingresar tres números
n1 = int(input("Ingrese el primer número: ")) # Solicitamos el primer número
n2 = int(input("Ingrese el segundo número: ")) # Solicitamos el segundo número
n3 = int(input("Ingrese el tercer número: ")) # Solicitamos el tercer número

prom = (n1 + n2 + n3) / 3 # Calculamos el promedio 
print(f"El promedio de los tres números es: {prom}") # Imprimimos el promedio