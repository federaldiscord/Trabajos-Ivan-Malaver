def sumar (a, b):
    resultado = (a + b)
    return resultado
total = sumar(5, 10)
print(total)

def restar (a, b):
    resultado = (a - b)
    return resultado
total2 = restar(10, 5)
print(total2)

def multiplicar (a, b):
    resultado = (a * b)
    return resultado
total3 = multiplicar(5, 10)
print(total3)

def dividir (a, b):
    resultado = (a / b)
    return resultado   
total4 = dividir(10, 5)
print(total4)

def potencia (a, b):
    resultado = (a ** b)
    return resultado
total5 = potencia(5, 2)
print(total5)