print("Vamos a encontrar y definir cuál de dos números es mayor.")
print("Digita el primer número.")
n1 = float(input("Primer número: "))
print("Digita el segundo número")
n2 = float(input("Segundo número: "))
if n1 > n2:
	print(f"El número mayor es: {n1}")
else:
	print(f"El número mayor es: {n2}")
